package JavaBasics;
public class Datatypes {
    public static void main(String[] args) {
        //integer data types
      int num=5;              //4 bytes
      short num1=127;         //2 bytes
      byte num2=127;          //1 byte
      long num3=5666l;         //8 bytes

        //floating point data types
        float num4=5.6f;      //4 bytes
        double num5=5.666;    //8 bytes
         
        //character data type
        char alphabet='A';     //2 bytes

        //boolean data type
        boolean a=true;        //1 byte
        boolean b=false;      //1 byte

    }
}
